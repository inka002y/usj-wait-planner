const BY_ID = new Map(USJ_ATTRACTIONS.map((attraction) => [attraction.id, attraction]));

const normalize = (value: string) =>
  value
    .normalize("NFKC")
    .toLowerCase()
    .replace(/[™®・～~\-–—:!'’.,\s]/g, "");

const BY_NAME = new Map<string, Attraction>();
for (const attraction of USJ_ATTRACTIONS) {
  BY_NAME.set(normalize(attraction.name), attraction);
  for (const alias of attraction.aliases ?? []) {
    BY_NAME.set(normalize(alias), attraction);
  }
}

export function getAttractionById(id: string): Attraction | undefined {
  return BY_ID.get(String(id));
}

export function findAttractionByName(name: string): Attraction | undefined {
  return BY_NAME.get(normalize(name));
}

export function getAttractionOrFallback(id: string, name: string): Attraction {
  const found = getAttractionById(id) ?? findAttractionByName(name);
  if (found) {
    return found;
  }

  return {
    id,
    name: name.trim(),
    area: "USJ",
    durationMinutes: 8,
    typicalWaitMinutes: 35,
    thrillLevel: 2,
    familyScore: 3,
    tags: ["live"],
    accentColor: "#D71920",
  };
}
